=head1 NAME

Wombat::Authenticator - authentication component interface

=head1 SYNOPSIS

=head1 DESCRIPTION

This interface specifies a component that provides some sort of
authentication service. The interface itself has no functional
significance but is used as a tagging mechanism so that other
components can detect the presence of an Authentication service.

